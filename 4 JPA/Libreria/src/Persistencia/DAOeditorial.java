package Persistencia;

import Entidades.Editorial;
import java.util.ArrayList;
import java.util.List;

public class DAOeditorial extends DAO<Editorial> {

    public DAOeditorial() {
    }

    public void guardarEditorial(Editorial editorial) {
        super.crear(editorial);
    }

    public void editarEditorial(Editorial editorial) {
        super.editar(editorial);
    }

    public void darDeBajaEditorial(Editorial editorial) {
        super.editar(editorial);
    }

    public void eliminarEditorial(Editorial editorial) {
        super.eliminar(editorial);
    }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public List buscarEditorial(String nombre) {
        List<Editorial> listaDeEditoriales = new ArrayList<>();
        try {
            super.conectarBD();
            listaDeEditoriales = em.createQuery("SELECT e FROM Editorial e WHERE e.nombre LIKE :nombre AND e.alta = 1 ", Editorial.class)
                    .setParameter("nombre", "%" + nombre + "%")
                    .getResultList();
        } catch (Exception e) {
            System.out.println("No se encontro el Editorial");
        } finally {
            super.desconectarBD();
        }
        return listaDeEditoriales;
    }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
