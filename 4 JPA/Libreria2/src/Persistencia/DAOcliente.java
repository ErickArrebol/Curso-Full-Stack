package Persistencia;

import Entidades.Cliente;

import javax.persistence.NoResultException;

public class DAOcliente extends DAO<Cliente> {

    public DAOcliente() {
    }

    public void guardarCliente(Cliente cliente) {
        super.crear(cliente);
    }

    public void editarCliente(Cliente cliente) {
        super.editar(cliente);
    }

    public void eliminarCliente(Cliente cliente) {
        super.eliminar(cliente);
    }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Cliente buscarCliente(long documento) {
        Cliente cliente = new Cliente();
        try {
            super.conectarBD();
            cliente = em.createQuery("SELECT c FROM Cliente c WHERE c.documento = :documento", Cliente.class)
                    .setParameter("documento", documento).getSingleResult();
            
        } catch (NoResultException e) {
            System.out.println("{NO SE ENCONTRO CLIENTE CON EL DOCUMENTO ESPECIFICADO}");
        } catch (Exception e) {
            System.out.println("{ERROR AL BUSCAR EL CLIENTE EN LA BASE DE DATOS}");
        } finally {
            super.desconectarBD();
        }
        return cliente;
    }
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
